-- เขียน SQL statement เเสดงรหัสอสังหาริมทรัพย์ เเละความคิดเห็นของลูกค้าที่มีต่ออสังหาริมทรัพย์ จากข้อมูลเกี่ยวกับอสังหาริมทรัพย์ที่ลูกค้าสนใจ โดยแสดงเฉพาะความเห็นของลูกค้าที่มีคำว่า '2%' อยู่ในตำแหน่งใดก็ได้ของความคิดเห็น
SELECT PropNo, Comment FROM interesting
WHERE Comment LIKE "%2$%%" ESCAPE '$';
